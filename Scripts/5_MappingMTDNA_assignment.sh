#We now need to filter out the sequence reads that do not match to the rCRS.
#The sam file we generated with bwa has ALL of the reads in it, whether they match the rCRS or not. 
#We use the program samtools to filter the reads that we want to keep. Later, we will also use samtools to merge multiple sequences from one individual into a single file.

