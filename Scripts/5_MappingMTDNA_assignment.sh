rm ${NAME}.mtDNA.mem.mapped.q30.bam
rm ${NAME}.mtDNA.mem.mapped.q30.sort.bam
rm ${NAME}.mtDNA.mem.mapped.q30.sort.rmdup.bam
rm ${NAME}.mtDNA.mem.sam

