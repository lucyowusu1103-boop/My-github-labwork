#One more variable to define, the name of your sam file:
NAME="CAL03_FGC2098_s_1_CGGCTATG-CCTATCCT" #change to your file name before .sam

#Step 1: Convert from sam to bam file (bam is the binary version of sam; helpful for big files)
samtools view -bSh -@ 2 ${NAME}.mtDNA.mem.sam > ${NAME}.mtDNA.mem.bam
#Run the code below to determine how many reads are present before filtering:
samtools view -c ${NAME}.mtDNA.mem.bam
#Question 5: How many reads are we starting with? 3398667

#Step 2: Filter out mapped reads 
samtools view -bh -F 4 -@ 2 ${NAME}.mtDNA.mem.bam > ${NAME}.mtDNA.mem.mapped.bam
#Question 6: Using the same samtools view -c code as above, find how many reads we have now (Hint: You will have to change the file name if you copy the code from above). 
samtools view -c ${NAME}.mtDNA.mem.mapped.bam
#Question 6: How many reads do we have now? 13540

#Step 3: Filter by q30 quality
samtools view -bh -q 30 ${NAME}.mtDNA.mem.mapped.bam > ${NAME}.mtDNA.mem.mapped.q30.bam
samtools view -c ${NAME}.mtDNA.mem.mapped.q30.bam
#Question 7: How many reads do we have now? 13528

#Step 4: Now we remove duplicates. First, we sort the reads by leftmost coordinate, then remove duplicates.
#Sorting alignment by leftmost coordinate
samtools sort -T ${NAME}.mtDNA.mem.mapped.q30.sort -o ${NAME}.mtDNA.mem.mapped.q30.sort.bam ${NAME}.mtDNA.mem.mapped.q30.bam
#Removing duplicates
samtools rmdup -s ${NAME}.mtDNA.mem.mapped.q30.sort.bam ${NAME}.mtDNA.mem.mapped.q30.sort.rmdup.bam
#Removing reads with multiple mappings
samtools view -bh -F 0x900 ${NAME}.mtDNA.mem.mapped.q30.sort.rmdup.bam > ${NAME}.mtDNA.mem.mapped.q30.sort.rmdup.uniq.bam
#Question 8: Now how many unique mapped reads with a quality of at least 30 do we have? (Hint: You'll want to look at the final file produced above)
samtools view -c ${NAME}.mtDNA.mem.mapped.q30.sort.rmdup.uniq.bam
# We have 9087 unique mapped reads with a quality of at least 30.

##We do not need all of the intermediary files we just created.
